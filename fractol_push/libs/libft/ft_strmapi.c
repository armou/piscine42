#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*str;

	if (!s || !f)
		return (NULL);
	i = 0;
	str = ft_strdup(s);
	while (str && str[i])
	{
		str[i] = f(i, str[i]);
		i++;
	}
	return (str);
}
